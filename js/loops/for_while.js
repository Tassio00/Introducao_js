
// Laços de repetição [ FOR, WHILE, DO .. WHILE, FOR .. IN, FOR .. OF ]

// for



for(let i = 0; i < 5; i++){
    if (i % 2 !== 0){
        console.log(i);
    }
}


// while 

let x = 5;

while (x < 5) {
    if(x % 2 !== 0){
        console.log(x);
    }
    x--;
    
}

// do .. while

let j = 0;
do {
    console.log(j);
    j++;
}
    
 while (j < 8);








